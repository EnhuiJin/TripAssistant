package com.example.tripassistant;

public class RestaurantModel {

	private String restName;
	private String restAddress;
	private int restTravelTime;
	private int restDuration;

	public String getRestName() {
		return restName;
	}

	public void setRestName(String restName) {
		this.restName = restName;
	}

	public String getRestAddress() {
		return restAddress;
	}

	public void setRestAddress(String restAddress) {
		this.restAddress = restAddress;
	}

	public int getRestTravelTime() {
		return restTravelTime;
	}

	public void setRestTravelTime(int restTravelTime) {
		this.restTravelTime = restTravelTime;
	}

	public int getRestDuration() {
		return restDuration;
	}

	public void setRestDuration(int restDuration) {
		this.restDuration = restDuration;
	}

}
